// Helper function to get chapters based on medium
export function getChaptersByMedium(className, subject, medium = 'hindi') {
  const classData = SYLLABUS_2025_LATEST[className];
  if (!classData) return [];

  const subjectData = classData[subject];
  if (!subjectData) return [];

  return subjectData[medium] || subjectData.hindi || subjectData.english || [];
}

